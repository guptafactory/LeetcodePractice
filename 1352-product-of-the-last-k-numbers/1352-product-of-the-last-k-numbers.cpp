class ProductOfNumbers {
    vector<int> arr;
public:
    ProductOfNumbers() {
        
    }
    
    void add(int num) {
        arr.push_back(num);
    }
    
    int getProduct(int k) {
        int n = arr.size(), i = n-1, prod = 1;
        while(k--) prod *= arr[i--];
        return prod;
    }
};

